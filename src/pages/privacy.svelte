<h2 id="how-we-use-cookies-and-tracking">How we use cookies and tracking</h2>
<p>WeCarry uses Google Analytics to help us understand how visitors interact with the app, but we do NOT use it to track, collect or send to Google any data that personally identifies an individual. For more information on how Google Analytics collects and processes data, see "How Google uses data when you use our partners' sites or apps" <a href="https://www.google.com/policies/privacy#partners/" target="_blank">https://www.google.com/policies/privacy/partners/</a>.</p>
<p>Other than any cookies used by Google Analytics (which we cannot see) and the session cookie automatically used by your browser to allow you to login (if applicable), WeCarry does not use cookies to collect any data about you.</p>
<p>While we may provide links to other websites, please be aware that we are not responsible for the content or privacy practices of third-party websites.</p>

<h2 id="where-we-host-our-service-and-store-your-information">Where we host our service and store your information</h2>
<p>WeCarry is hosted on Amazon Web Services (AWS) in their US East region, specifically in Virginia, U.S. AWS provides highly secure and resilient services that meet many if not all industry standards and regulations. To learn more about AWS and their security practices and certifications visit <a href="https://aws.amazon.com" target="_blank">https://aws.amazon.com</a>. Each of the other vendors mentioned above operate and manage their own servers and services. More information about each can be found on their own websites. </p>

<h2 id="how-we-secure-your-information">How we secure your information</h2>
<p>We believe in a defense-in-depth strategy that leverages multiple layers of security to provide the best level of protection available. We utilize a web-application firewall that inspects all traffic for malicious intent as well as blocks known bad actors and emerging attacks. We use strict firewall rules that limit access to only traffic that has gone through the web-application firewall. Our servers are managed in what is called a "virtual private cloud" which means they exist on a private network where no other traffic is able to reach them. Further, your information stored in our database is encrypted both in transit and at rest. Finally, we operate with a least-access privilege model which restricts access to servers and services to only the individuals or other services which need access to them. All access into and out of the network is logged as well as administrative access to servers and services. We even remove location information from any photos you choose to upload to WeCarry. We're not only the creators of WeCarry, we're users too. We trust the platform with our needs and we believe you can too. </p>

<h2 id="our-global-privacy-practices">Our global privacy practices</h2>
<p>We store and process the information that we collect in the United States in accordance with this Privacy Policy (our vendor services may store and process data outside the United States). However, we understand that we have users from different countries and regions with different privacy expectations, and we try to meet those needs even when the United States does not have the same privacy framework as other countries.</p>
<p>We provide a high standard of privacy protection—as described in this Privacy Policy—to all our users around the world, regardless of their country of origin or location, and we are proud of the levels of notice, choice, accountability, security, data integrity, access, and recourse we provide. We work hard to comply with the applicable data privacy laws wherever we do business, working with our Data Protection Officer as part of a cross-functional team that oversees our privacy compliance efforts. </p>

